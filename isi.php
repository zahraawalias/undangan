    <script>
        // Initialize AOS
        AOS.init({
            duration: 1200,
            once: false,
            offset: 50,
            easing: 'ease-in-out'
        });

        const items = document.querySelectorAll('.story-item');

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const index = [...items].indexOf(entry.target);

                    setTimeout(() => {
                        entry.target.classList.add('active');
                    }, index * 200); // delay bertahap
                }
            });
        }, {
            threshold: 0.3
        });

        items.forEach(item => observer.observe(item));

        // Countdown Timer
        const weddingDate = new Date('2026-02-01T09:00:00+07:00').getTime();

        function updateCountdown() {
            const now = new Date().getTime();
            const distance = weddingDate - now;

            const days = Math.floor(distance / (1000 * 60 * 60 * 24));
            const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
            const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
            const seconds = Math.floor((distance % (1000 * 60)) / 1000);

            document.getElementById('days').textContent = String(days).padStart(2, '0');
            document.getElementById('hours').textContent = String(hours).padStart(2, '0');
            document.getElementById('minutes').textContent = String(minutes).padStart(2, '0');
            document.getElementById('seconds').textContent = String(seconds).padStart(2, '0');
        }

        updateCountdown();
        setInterval(updateCountdown, 1000);

        // Love Story Animation
        const stories = document.querySelectorAll('.story-item');
        const arrow = document.querySelector('.story-arrow');
        let currentIndex = 0;

        function updateStory(index) {
            stories.forEach((story, i) => {
                story.classList.toggle('active', i === index);
            });

            const activeStory = stories[index];
            arrow.style.transform = `translateY(${activeStory.offsetTop}px)`;
        }

        updateStory(currentIndex);
        setInterval(() => {
            currentIndex = (currentIndex + 1) % stories.length;
            updateStory(currentIndex);
        }, 4000);

        // Gift Toggle
        function toggleGift() {
            const content = document.getElementById('giftContent');
            content.style.display = content.style.display === 'block' ? 'none' : 'block';
        }

        // Copy Text
        function copyText(id, btn) {
            const text = document.getElementById(id).innerText;
            navigator.clipboard.writeText(text).then(() => {
                const original = btn.innerHTML;
                btn.innerHTML = '<i class="bi bi-check"></i> Tersalin';
                setTimeout(() => btn.innerHTML = original, 1500);
            });
        }


        // Submit Wish
        function submitWish() {
            const name = document.getElementById('wishName').value.trim();
            const message = document.getElementById('wishMessage').value.trim();
            const list = document.getElementById('wishList');

            if (!name || !message) {
                alert('Nama dan ucapan tidak boleh kosong ü§ç');
                return;
            }

            const wishItem = document.createElement('div');
            wishItem.classList.add('wish-item');
            wishItem.innerHTML = `
                <h6>${name}</h6>
                <p>${message}</p>
            `;

            list.prepend(wishItem);

            document.getElementById('wishName').value = '';
            document.getElementById('wishMessage').value = '';
        }
    </script>
